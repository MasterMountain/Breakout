package sprites;

public abstract class Sprite {
    protected int x;
    protected int y;
    protected final int height;
    protected int width;

    public Sprite(int x, int y, int height, int width) {
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
    }
    
    protected void setImage() {
        
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

}
